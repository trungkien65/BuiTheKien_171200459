package com.example.customlistview;

public class SinhVien {
    public  String HoTen;
    public  String NamSinh;

    public SinhVien(String hoTen, String namSinh) {
        HoTen = hoTen;
        NamSinh = namSinh;
    }

    public String getHoTen() {
        return HoTen;
    }

    public String getNamSinh() {
        return NamSinh;
    }
}
