package com.example.contraintslayout;

public class sinhvien {
    private String HoTen;
    private String NamSinh;

    public sinhvien(String hoTen, String namSinh) {
        HoTen = hoTen;
        NamSinh = namSinh;
    }

    public String getHoTen() {
        return HoTen;
    }

    public String getNamSinh() {
        return NamSinh;
    }
}
